function changeLanguage(lang) {
    for (const key in translatedElements) {
        const element = document.getElementById(key);
        if (element) {
            if (translatedElements[key][lang]) {
                // Update the text content of the element or its children
                if (element.classList.contains('product')) {
                    // For product elements, find the child <p> element
                    const productDescription = element.querySelector('p');
                    productDescription.textContent = translatedElements[key][lang];
                } else {
                    element.textContent = translatedElements[key][lang];
                }
            }
        }
    }
}


